    private void btn_hazir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hazir1ActionPerformed
        //siyah oyuncunun hazir olup olmamasi
        kullanici1.email = txt_email1.getText();
        kullanici1.kullanici_Adi = txt_kullanici_adi1.getText();
        kullanici1.parola = new String(ptxt_sifre1.getPassword());

        boolean isLogin = AliEnesGurelData.Login(kullanici1.email, kullanici1.kullanici_Adi, kullanici1.parola);
        if (isLogin) {
            if (kullanici1.kullanici_Adi.equals(txt_kullanici_adi2.getText()) && kullanici1.parola.equals(new String(ptxt_sifre2.getPassword())) && !txt_kullanici_adi1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "kullanici adiniz beyaz oyuncu ile ayni olamaz!");
            } else {
                lbl_siyahBekleniyor.setText("HAZIR");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Böyle bir kullanıcı bulunmamaktadır! İlk önce kayıt olmanız gerekmektedir.");
        }
        if (isLogin && lbl_beyazBekleniyor.getText().equals("HAZIR") && lbl_siyahBekleniyor.getText().equals("HAZIR")) {
            AliEnesGurelFrm_GameScene oyun_ekrani = new AliEnesGurelFrm_GameScene();
            oyun_ekrani.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_hazir1ActionPerformed

