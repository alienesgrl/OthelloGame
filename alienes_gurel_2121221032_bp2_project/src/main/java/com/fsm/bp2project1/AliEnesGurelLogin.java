        boolean isLogin = AliEnesGurelData.Login(kullanici2.email, kullanici2.kullanici_Adi, kullanici2.parola);
        if (isLogin) {
            if (kullanici2.kullanici_Adi.equals(txt_kullanici_adi1.getText()) && kullanici2.parola.equals(new String(ptxt_sifre1.getPassword())) && !txt_kullanici_adi2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "kullanici adiniz siyah oyuncu ile ayni olamaz!");
            } else {
                lbl_beyazBekleniyor.setText("HAZIR");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Böyle bir kullanıcı bulunmamaktadır! İlk önce kayıt olmanız gerekmektedir.");
        }
        if (isLogin && lbl_beyazBekleniyor.getText().equals("HAZIR") && lbl_siyahBekleniyor.getText().equals("HAZIR")) {
            AliEnesGurelFrm_GameScene oyun_ekrani = new AliEnesGurelFrm_GameScene();
            oyun_ekrani.setVisible(true);
            this.dispose();
        }
