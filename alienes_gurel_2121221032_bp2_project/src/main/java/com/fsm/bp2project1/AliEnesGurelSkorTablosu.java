    private void btn_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_silActionPerformed
        DefaultTableModel scoreTable = (DefaultTableModel) tbl_skorTablosu.getModel();//sil butonuna basilmasi
        int secilen = tbl_skorTablosu.getSelectedRow();
        String ad = scoreTable.getValueAt(secilen, 0).toString();
        if (ad.equals(login.getKullanici1().kullanici_Adi)) {
            kontrolluSil(login.getKullanici1());
        } else if (ad.equals(login.getKullanici2().kullanici_Adi)) {
            kontrolluSil(login.getKullanici2());
        } else {
            //berabere durumunda bilgi isteme.
            scoreTable.removeRow(secilen);
            txtdenSkorSil(secilen);
        }
    }//GEN-LAST:event_btn_silActionPerformed

    public void kontrolluSil(AliEnesGurelKullanici kullanici) {
        boolean sildiMi = false;//bazi kontroller sonucu tablodan skor siler
        DefaultTableModel scoreTable = (DefaultTableModel) tbl_skorTablosu.getModel();
        int secilen = tbl_skorTablosu.getSelectedRow();
        String mailOnayla = JOptionPane.showInputDialog(rootPane, "Lutfen e-mail adresinizi giriniz.", JOptionPane.INFORMATION_MESSAGE);
        if (mailOnayla.compareTo(kullanici.email) == 0) {
            String parolaOnayla = JOptionPane.showInputDialog(rootPane, "Lutfen parolanizi giriniz.", JOptionPane.INFORMATION_MESSAGE);
            if (parolaOnayla.compareTo(kullanici.parola) == 0) {
                scoreTable.removeRow(secilen);
                sildiMi = true;
            } else {
                JOptionPane.showMessageDialog(rootPane, "hatali parola");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "hatali e-mail adresi");
        }
        if (kullanici.email.equals("OYUN")) {
            sildiMi = true;
        }
        if (sildiMi == true) {
            //burada txt den silmeliyim.
            txtdenSkorSil(secilen);
        }
    }

    public void txtdenSkorSil(int secilen) {
        try {
            AliEnesGurelData.skorListesi.remove(secilen);
            AliEnesGurelData.DosyayaSkorYazdirma();
            JOptionPane.showMessageDialog(null, "Skor basariyla silinmistir.");
        } catch (IOException ex) {
            Logger.getLogger(AliEnesGurelSkorTablosu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

